// Funci√≥n helper para mostrar la imagen en el display
// Uso: showLogoImage(dma_display);
inline void showLogoImage(MatrixPanel_I2S_DMA* display) {
    for (int y = 0; y < 64; y++) {
        for (int x = 0; x < 64; x++) {
            int idx = (y * 64 + x) * 3;
            uint8_t r = pgm_read_byte(&LOGO_DATA[idx]);
            uint8_t g = pgm_read_byte(&LOGO_DATA[idx + 1]);
            uint8_t b = pgm_read_byte(&LOGO_DATA[idx + 2]);
            display->drawPixel(x, y, display->color565(r, g, b));
        }
    }
}